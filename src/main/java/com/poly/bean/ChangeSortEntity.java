package com.poly.bean;

public class ChangeSortEntity {
	
	private int nowid;
	
	private int changeid;
	
	private int nowsort;
	
	private int changesort;

	public int getNowid() {
		return nowid;
	}

	public void setNowid(int nowid) {
		this.nowid = nowid;
	}

	public int getChangeid() {
		return changeid;
	}

	public void setChangeid(int changeid) {
		this.changeid = changeid;
	}

	public int getNowsort() {
		return nowsort;
	}

	public void setNowsort(int nowsort) {
		this.nowsort = nowsort;
	}

	public int getChangesort() {
		return changesort;
	}

	public void setChangesort(int changesort) {
		this.changesort = changesort;
	}

}
